typedef struct  {
    Cipher_Algorithm         algor;
    
    int         keysize;
    uint8_t        *key;
    uint8_t*       PT;			/* Plaintext			*/
    size_t		PTlen;
    uint8_t*       IV;			/* Init Vector			*/
    uint8_t*       EBC;		/* EBC	Known Answer	*/
    size_t		EBClen;
    uint8_t*       CBC;		/* CBC Known Answer		*/
    size_t		CBClen;
     uint8_t*       CBCpad;		/* CBC Padded Known Answer		*/
    size_t         CBCPadlen;
    
    
} katvector;



static S4Err RunCipherKAT(  katvector *kat)

{
    S4Err err = kS4Err_NoErr;
    char* name = NULL;
    uint8_t *out = NULL;
    
    size_t  alloc_len =   MAX(kat->EBClen, kat->CBClen);
    
    out = ( uint8_t *) malloc(alloc_len);
    ZERO(out, alloc_len);
    
    
    //   err = cipher_is_valid(kat->algor); CKERR;
    
    name = cipher_algor_table(kat->algor);
    
    OPTESTLogInfo("\t%-12s ", name);
    
    if(kat->PT && kat->PTlen > 0)
    {
        
        OPTESTLogInfo("%4s", "ECB");
        
        err = ECB_Encrypt(kat->algor, kat->key, kat->PT,kat->PTlen, out); CKERR;
        
        /* check against know-answer */
        err = compareResults( kat->EBC, out, kat->EBClen , kResultFormat_Byte, "Symmetric Encrypt"); CKERR;
        
        err = ECB_Decrypt(kat->algor, kat->key, out,kat->PTlen, out); CKERR;
        
        /* check against orginal plain-text  */
        err = compareResults( kat->PT, out, kat->PTlen , kResultFormat_Byte, "Symmetric Decrypt"); CKERR;
    }
    
    if(kat->CBC && kat->CBClen > 0)
    {
        CBC_ContextRef CBC;
        
        OPTESTLogInfo("%4s", "CBC");
        err = CBC_Init(kat->algor, kat->key, kat->IV,   &CBC ); CKERR;
        
        err = CBC_Encrypt(CBC, kat->PT, kat->PTlen, out); CKERR;
        
        /* check against know-answer */
        err = compareResults( kat->CBC, out, kat->CBClen , kResultFormat_Byte, "CBC Encrypt"); CKERR;
        
        // reset CBC befire decrypt*/
        CBC_Free(CBC);
        
        err = CBC_Init(kat->algor, kat->key, kat->IV,   &CBC ); CKERR;
        
        err = CBC_Decrypt(CBC,out, kat->PTlen, out); CKERR;
        
        /* check against orginal plain-text  */
        err = compareResults( kat->PT, out, kat->PTlen , kResultFormat_Byte, "CBC Decrypt"); CKERR;
        
        CBC_Free(CBC);
    }
  
    if(kat->CBCpad && kat->CBCPadlen > 0)
    {
        uint8_t *CT = NULL;
        size_t CTLen = 0;
  
        uint8_t *PT = NULL;
        size_t PTLen = 0;
        
        OPTESTLogInfo("%8s", "CBC/PAD");
   
        err = CBC_EncryptPAD(kat->algor, kat->key, kat->keysize >>3, kat->IV,
                            kat->PT, kat->PTlen,
                             &CT, &CTLen); CKERR;
       
        /* check against know-answer */
        err = compare2Results( kat->CBCpad,kat->CBCPadlen, CT, CTLen , kResultFormat_Byte, "CBC/PAD Encrypt"); CKERR;
        

        err = CBC_DecryptPAD(kat->algor, kat->key, kat->keysize >>3, kat->IV,
                             CT, CTLen,
                             &PT, &PTLen); CKERR;
     
        /* check against orginal plain-text  */
        err = compareResults(kat->PT, PT, kat->PTlen, kResultFormat_Byte, "CBC/PAD Decrypt"); CKERR;

      }
    
    
done:
    
    
    free(out);
    
    OPTESTLogInfo("\n");
    return err;
}


